package java_project1;

public class Calculator {

	private int num1=7;
	private int num2=3;
	private int sum;
	private int product;
	private int modulus;
	
	
	 int sum() {
		 sum = num1+num2;
		 return sum;
	}
	
	int multiplication() {
		product = num1*num2;
		return product;
	
	}
	
	int modulus() {
		modulus = num1%num2;
		return modulus;
	}
	
	
		
}
